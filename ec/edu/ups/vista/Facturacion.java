    private void txtCedulaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCedulaKeyPressed
       if (evt.getKeyCode() == KeyEvent.VK_ENTER){
           ControlClientes controlClientes=new ControlClientes();
           
           Clientes clientes=controlClientes.buscarCliente(txtCedula.getText());
         if(clientes.getCedula().equals("")){    
           int resp = JOptionPane.showConfirmDialog(null, "Cliente no existe", "Alerta!", JOptionPane.YES_NO_OPTION);
           
           
       }else{
           txtCliente.setText(clientes.getNombre());
           txtTelefono.setText(clientes.getTelefono());
           txtDireccion.setText(clientes.getDireccion());
           this.clientes=clientes;   
       }
       }
    }//GEN-LAST:event_txtCedulaKeyPressed

