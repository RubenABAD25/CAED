    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
        ArrayList<FacturaDetalle> facturaDetalle=new ArrayList<FacturaDetalle>();
        CabeceraFac cabecera=new CabeceraFac();
        
            cabecera.setFecha(lbFecha.getText());
            cabecera.setSubtotal(Double.parseDouble(txtSubtotal.getText()));
            cabecera.setIva(Double.parseDouble(txtIva.getText()));
            cabecera.setDescuento(0);
            cabecera.setTotal(Double.parseDouble(txtTotal.getText()));
        
            for (int i=1; i<=(tblDetalle.getRowCount())-2 ; i++){
                FacturaDetalle facDeta=new FacturaDetalle();
                
                facDeta.setCodigo((int) tblDetalle.getValueAt(i+1, 0));
                facDeta.setDescripcion((String) tblDetalle.getValueAt(i+1, 1));
                facDeta.setCantidad((int) tblDetalle.getValueAt(i+1, 2));
                facDeta.setPrecioU((double) tblDetalle.getValueAt(i+1, 3));
                facDeta.setTotal((double) tblDetalle.getValueAt(i+1, 4));
                
                facturaDetalle.add(facDeta);
            }
        
            Pago pagos =new Pago();
            pagos.setCabecera(cabecera);
            pagos.setClientes(clientes);
            pagos.setCodigoEmpleado(codigoEmpleado);
            pagos.setFactura(facturaDetalle);
            pagos.setVisible(true);
            pagos.getTxtTotal().setText(txtTotal.getText());
            pagos.setFacturaVen(this);
          //busPro.setVisible(true);
            
        
        
    }//GEN-LAST:event_btnProcesarActionPerformed
    
